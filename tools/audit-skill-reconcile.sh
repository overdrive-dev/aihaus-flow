# ---- --concat mode -----------------------------------------------------------
cmd_concat() {
  # Collect fragment files in deterministic alphabetical order (aih-* then _shared)
  local fragments=()
  while IFS= read -r -d '' f; do
    fragments+=("$f")
  done < <(find "$FRAGMENT_DIR" -maxdepth 1 -name "aih-*.md" -print0 | sort -z)
  # Append _shared.md last
