    fragments+=("${FRAGMENT_DIR}/_shared.md")
  fi

  if [ "${#fragments[@]}" -eq 0 ]; then
    echo "audit-skill-reconcile.sh: no fragment files found in $FRAGMENT_DIR" >&2
    exit 1
