#!/usr/bin/env bash
# audit-skill-reconcile.sh — SKILL enforcement-audit concat + score-consistency (M021/S08)
#
# Usage:
#   bash tools/audit-skill-reconcile.sh --concat
#       Regenerate canonical enforcement-audit.md from all 13+1 fragment files.
#       Deterministic: re-run produces byte-identical output.
#
#   bash tools/audit-skill-reconcile.sh --score-consistency-check
#       Flag rows where >=1 score axis diverges for known-similar step groups.
#       Advisory output only — exits 0 even with findings (per L7).
#
#   bash tools/audit-skill-reconcile.sh --reconcile
#       Run --concat + --score-consistency-check + duplicate-row check +
#       per-SKILL >=3-row floor assertion.
#
# Architecture: M021/S08 — owned file. Does NOT modify audit-skill-enforcement.sh (S01).
# ADR references: ADR-260503-A (framework), ADR-004 (single-writer), ADR-260502-A (eligibility)

